import mongoose from "mongoose";

const contactTicketSchema = mongoose.Schema({
  tittle: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("contactTickets", contactTicketSchema);
